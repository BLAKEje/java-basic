package practice;

public class operadores1 {
    public static void main(String[] args) {
        int numero = 10;
        ++ numero;
        System.out.println(numero++);
        System.out.println(numero--);
        System.out.println(numero);

        boolean variavel = true;
        System.out.println(!variavel);
        System.out.println(variavel);
        variavel = !variavel;
        System.out.println(variavel);

    }
}
